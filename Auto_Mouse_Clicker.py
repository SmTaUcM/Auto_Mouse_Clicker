import time
import threading
from pynput.mouse import Button, Controller
from pynput.keyboard import Listener, KeyCode
#----------------------------------------------------------------------------------------------------------------------------------------------------#


#----------------------------------------------------------------------------------------------------------------------------------------------------#
#                                                                      Classes.                                                                      #
#----------------------------------------------------------------------------------------------------------------------------------------------------#
class ClickMouse(threading.Thread):
    '''Mouse object'''

    def __init__(self, delay, button):
        '''Class constructor - obj = ClickMouse(1.5, Button.left)'''

        super(ClickMouse, self).__init__()
        self.delay = delay
        self.button = button
        self.running = False
        self.program_running = True
        #--------------------------------------------------------------------------------------------------------------------------------------------#

    def start_clicking(self):
        '''Method to handle start actions.'''

        self.running = True
        #--------------------------------------------------------------------------------------------------------------------------------------------#

    def stop_clicking(self):
        '''Method to handle stop actions.'''

        self.running = False
        #--------------------------------------------------------------------------------------------------------------------------------------------#

    def exit(self):
        '''Method to handle exit actions.'''

        self.stop_clicking()
        self.program_running = False
        #--------------------------------------------------------------------------------------------------------------------------------------------#

    def run(self):
        '''Method to handle normal running actions.'''

        while self.program_running:
            while self.running:
                mouse.click(self.button)
                time.sleep(self.delay)
            time.sleep(0.1)
        #--------------------------------------------------------------------------------------------------------------------------------------------#
    #------------------------------------------------------------------------------------------------------------------------------------------------#
#----------------------------------------------------------------------------------------------------------------------------------------------------#


#----------------------------------------------------------------------------------------------------------------------------------------------------#
#                                                                      Functions.                                                                    #
#----------------------------------------------------------------------------------------------------------------------------------------------------#
def on_press(key):
    '''Function that handles key presses.'''

    if key == start_stop_key:

        if click_thread.running:
            click_thread.stop_clicking()

        else:
            click_thread.start_clicking()

    elif key == exit_key:
        click_thread.exit()
        listener.stop()
    #------------------------------------------------------------------------------------------------------------------------------------------------#
#----------------------------------------------------------------------------------------------------------------------------------------------------#


#----------------------------------------------------------------------------------------------------------------------------------------------------#
#                                                                     Main Program.                                                                  #
#----------------------------------------------------------------------------------------------------------------------------------------------------#
delay = 10 # Time delay between clicks in seconds.
button = Button.left # Button to press after each delay.
start_stop_key = KeyCode(char='s')
exit_key = KeyCode(char='e')

mouse = Controller()
click_thread = ClickMouse(delay, button)
click_thread.start()

with Listener(on_press=on_press) as listener:
    listener.join()
